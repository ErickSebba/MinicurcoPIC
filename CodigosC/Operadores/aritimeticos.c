#include <stdio.h>
int main() {
    int a = 10, b = 3;
    printf("Soma: %d\n", a + b);           // Resultado: 13
    printf("Subtração: %d\n", a - b);     // Resultado: 7
    printf("Multiplicação: %d\n", a * b); // Resultado: 30
    printf("Divisão: %d\n", a / b);       // Resultado: 3
    printf("Módulo: %d\n", a % b);        // Resultado: 1
    return 0;
}